pub(crate) fn lane(
    id: &str,
    role: &str,
    model: &str,
    model_display: &str,
    receives: &[&str],
    focus: &str,
) -> LanePlan {
    LanePlan {
        id: id.to_owned(),
        role: role.to_owned(),
        model: model.to_owned(),
        model_display: model_display.to_owned(),
        receives: receives.iter().map(|value| (*value).to_owned()).collect(),
        focus: focus.to_owned(),
    }
}
