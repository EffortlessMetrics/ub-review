#[expect(
    clippy::too_many_arguments,
    reason = "tracked in policy/allow.toml#clippy-too-many-arguments-artifact-writers"
)]
pub(crate) fn tool(
    id: &str,
    command: &str,
    class: ToolClass,
    weight: u32,
    trigger: Trigger,
    timeout_sec: u64,
    artifact_budget_mb: u64,
    requires_lease: bool,
    enabled: bool,
) -> ToolPolicy {
    ToolPolicy {
        id: id.to_owned(),
        command: command.to_owned(),
        class,
        weight,
        default: trigger,
        required: false,
        timeout_sec,
        artifact_budget_mb,
        requires_lease,
        enabled,
        gate: None,
        // `provided` means "the repo config explicitly wrote this field".
        // Built-in tools were written by nobody's repo config, so every flag
        // is false: profile-level `[tool_timeouts]` overrides apply to them,
        // while a repo's explicit `[tools.<id>] timeout_sec` (provided =
        // true after the merge) stays the per-repo winner.
        provided: ToolPolicyProvided::default(),
    }
}

